<div class="help_title">Inserting a Link Into the Page Content</div>
<p>
	Inserting a hyperlink, or link, into a page is very easy. The first thing you will want to do is write the text that
	you wish to become a link. Below, the user had entered the text, "Click here to visit Google."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-link/text.png" />
</p>

<p>
	Highlight the text with your mouse, like the user below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-link/text-highlighted.png" />
</p>

<p>
	Now, click the create link button, which is outlined in red (it is a chain link icon).
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-link/link-button.png" />
</p>

<p>
	Enter the URL that you want the link to take the user to.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-link/link-box.png" />
</p>

<p>
	Click "Insert." The link will appear in the editor, but it will not be possible to click on it in the editor. The link
	will only work on the published version of the page.
</p>