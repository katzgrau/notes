<div class="help_title">Rearranging The Link Order On Your Website</div>
<p>
	You may want to edit the order of links on your website. In the picture below,
	the user has several links on his website.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-link-order/links.png" />
</p>

<p class="help_message">
	To learn how to add links to your website, <a href="<?= config_item('base_url') ?>/help/site/inserting-a-link">click here</a>.
</p>

<p>
	Let's say that the user wants to move the "Bibliography Help" link to the top of the list. Go to the site settings page,
	and scroll to the area labeled "Link Order," which is outlined in red below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-link-order/link-order.png" />
</p>

<p>
	Click and drag the link you would like to move to the order in the list you wish it to be. In the image below,
	the user will click and drag "Bibliography Help" to the top of the list.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-link-order/reorder.png" />
</p>

<p>
	It is not necessary to click "Save Settings." The links are now reordered on your website, as in the example website below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-link-order/final.png" />
</p>