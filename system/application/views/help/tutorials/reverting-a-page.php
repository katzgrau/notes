<div class="help_title">Reverting A Page</div>
<p>
	If you find yourself editing one of your pages, and no longer want or need the changes you have made, you might want to revert back to the
	published version of a page.
</p>

<p>
	For example, a user might inadvertently delete the content of the draft version of the page, but has not yet published the changes. A time like this is when
	a user might wish to revert the draft version of the page back to the published version.
</p>

<p class="help_warning">
	All changes made to the draft version of a page will be lost, and the published version's content will be restored.
</p>

<p>
	To do this, open up the page that you want to revert for editing. If the page is a draft, a "Revert to Published" button will
	appear on the bottom right. It is pictured below. Click it, and the published version of the page will be restored.
</p>

