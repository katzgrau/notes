<div class="help_title">Inserting A Picture From Your Computer</div>
<p>
	Uploading a picture from your computer to your website is simple. First, open the page that
	you want to edit. Then click on the toolbar button which is labeled "Insert An Image."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/uploading-a-picture/insert-image-button.png" />
</p>

<p>
	When you click on this button, a window will appear on the center of the screen.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/uploading-a-picture/insert-image-popup.png" />
</p>

<p>
	Click "browse" to open a file choosing box. Choose the image file on your computer that you want to
	insert into your webpage.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/uploading-a-picture/save-as.png" />
</p>

<p>
	Once you select the file, and click "open," consider the settings in the popup box. If the image is large in display size,
	you can choose to have it automatically resized to fit the page by checking the checkbox immediately under the browse button.
</p>

<p>
	You can also choose how you would like the picture aligned. You can choose left (default), center, or right. Once you
	are finished, you can click "Insert image." You may have to wait for a moment while the image is copid from your computer
	to <?= config_item('company_name') ?>.
</p>

<p class="help_warning">
	The system will check to see that you have uploaded a valid image, and that you haven't exceed your webspace alotment.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/uploading-a-picture/image-settings.png" />
</p>

<p>
	Once the copy is complete, your picture will be insertd into the page.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/uploading-a-picture/complete.png" />
</p>
