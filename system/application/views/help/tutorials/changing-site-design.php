<div class="help_title">Changing Your Website's Design</div>
<p>
	To change the design of your website, you will want to go to the site settings page.
	This is pictured below, where the user is clicking on the "Site Settings" link.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-site-design/site-settings.png" />
</p>

<p>
	Once you are viewing the site settings page you will see the area labeled "Site Design." This is pictured below.
	Small pictures of the designs you can choose from will be displayed. To view more designs, click "Get Next."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-site-design/designs.png" />
</p>

<p>
	When you find a design that you like, click on it. A window previewing your website in the new design will appear in the
	center of the screen. This is pictured below.	
	If you would like to use that design with your website, click
	"Activate This Theme."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-site-design/preview.png" />
</p>

<p>
	Your website's design will have been changed.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-site-design/final.png" />
</p>