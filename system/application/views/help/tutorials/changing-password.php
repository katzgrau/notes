<div class="help_title">Changing Your Account Password</div>
<p>
	To change your password, go to the "Edit Account" tab, as pictured below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-account-information/account.png" />
</p>

<p>
	Navigate to the area labeled "Password." On the right side, you will see a link that says "Click to Change Password." Click it,
	as pictured below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-password/change-password.png" />
</p>

<p>
	The menu will expand, and a password changing area will appear. Enter you current password, and your
	new password twice. When you are complete, press enter, or click "Save Account Information."
</p>

<p class="help_warning">
	Make sure your password has a minimum length of <?= config_item('min_password_length') ?>, and that you type your new 
	password correctly both times. You will see an error message otherwise.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-password/changing.png" />
</p>

