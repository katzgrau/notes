<div class="help_title">Inserting An HTML Snippet</div>
<p>
	This topic is for slightly more advanced users, but the general idea is simple. Suppose you have
	and HTML code snippet from a website such as YouTube, Flickr, or other source, and you need to
	embed it into your page. We'll show you how to do that.
</p>

<p class="help_message">
	A full example of inserting a YouTube video into a webpage via HTML snippet is available 
	by <a href="<?= config_item('base_url') ?>help/edit/inserting-a-youtube-video">clicking here</a>.
</p>

<p>
	First, open a new page, or an existing page in the authoring panel for editing. Next, click on the HTML editor button.
	This button is outlined in red below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/html-button.png" />
</p>

<p>
	Next, enter the snippet you were provided into the textbox that pops up, and click "Update."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/source-editor.png" />
</p>

<p>Once this is done, and you publish your web page, your embedded object should be visible publicly. It will not, however, be immediately
visible in the editor after you click "Update."</p>
