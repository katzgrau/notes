<div class="help_title">Inserting A Link On Your Website (In the sidebar)</div>
<p>
	This tutorial is for inserting a link on your website that will appear on the side or 
	menu portion of your website, similar to the area on the example website pictured below outlined in red.
</p>

<p class="help_message">
	If you are editing a webpage, and you would like to insert a link into the content portion of the page,
	<a href="<?= config_item('base_url') ?>help/edit/creating-a-link">click here</a> to learn how to do that.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-link/links.png" />
</p>


<p>
	Navigate to the Site Settings page, and scroll to the area labeled "Add Links." This area is outlined in red in the 
	picture below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-link/insert-link.png" />
</p>

<p>
	Enter the URL, or address of the website you wish to create a link to in the field labeled "URL." Next, enter
	the display name of the link into the field labeled "title." In the example below, a link will be created to the
	website www.google.com, but the actual text the user clicks will be "Google."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-link/enter-info.png" />
</p>

<p>
	Click "Add Link" immediately below the Title field, or click "Save Settings." Your new link will now appear on the
	"Link Order" list above the "Add Link" area, like below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-link/added.png" />
</p>

<p>
	The link will now automatically appear on your website, like below.
<p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-link/after.png" />
</p>

<p>
	To rearrange the order of the links, click and drag the link in the "Link Order" listing to the position you
	choose. You can read more about that by <a href="<?= config_item('base_url') ?>help/site/editing-link-order">clicking here</a>.
</p>
