<div class="help_title">Changing the Font</div>
<p>
	Changing the font of the text in the page editor is very much like changing it in any standard word processor.
	Typically, you have two choices when you want to choose the font style.
</p>

<ol>
	<li>Change the font settings <b>before</b> you begin typing</li>
	<li>Change the font settings <b>after</b> you begin typing</li>
</ol>

<p>
	Let's say you have a blank page, and we want the text we write to be large, bold, italic, and of the font "Verdana."
	To do this, we must first choose a large font size. There is a menu which is labeled "Font size" on the editor.
	We will click the down arrow to the right. This is pictured below. Scroll down and click 7 (36 Point).
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-the-font/font-size.png" />
</p>

<p>
	Next, we'll click the B button, which stands for bold. Likewise, we'll click the I button, which stands for italic.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-the-font/bold.png" />
</p>

<p>
	Finally we'll change the font family, which is accessed via a drop-down menu like the font size. The menu is labeled
	"Font family." We'll scroll down and click "Verdana."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-the-font/font-family.png" />
</p>

<p>
	Now, when we type text, it will be in the font and style that we set.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-the-font/changed.png" />
</p>

<p>
	Of course, we can do this process differently. If we choose, we can type the text first, then highlight it
	with the cursor, and change the font settings (like we did above). That will work too!