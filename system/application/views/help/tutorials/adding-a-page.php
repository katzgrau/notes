<div class="help_title">Creating A New Page</div>
<p>
	In the "Edit Pages" screen, there are two ways to add a page to your website. 
	One way to is to click on the "New Page" button in the toolbar right above
	the content pane. This is pictured below.
</p>
<p>
	<img src="<?= config_item('base_url') ?>public/help/adding-a-page/add-toolbar.png" alt="" class="with_picture_frame" />
</p>

<p>
	The second way to add a page is to click the "New Page" link in the sidebar on the right of the page.
	This is pictured below.
</p>

<p>
	<img src="<?= config_item('base_url') ?>public/help/adding-a-page/add-pagelist.png" alt="" class="with_picture_frame" />
</p>

<p>
	When you click New Page, the Edit Pages screen will refresh with a blank title and empty content area.
	From here, you can enter a title for your new page, and add content as well. If you wish to save the changes
	you make, but you do not want the content to be publicly visible, you can click "Save Draft" in the upper
	right part of the screen. You can read more about saving drafts by
	<a href="<?= config_item('base_url') ?>help/introduction/saving-a-draft">clicking here</a>.
</p>

<p>
	<img src="<?= config_item('base_url') ?>public/help/adding-a-page/add-entry.png" alt="" class="with_picture_frame" />
</p>

<p>
	When you want your changes to be visible to the public, you can click "Publish." Your page
	title and its content will automatically be added and linked to your website. You can now
	view your website and see your new page! You can read more about publishing pages by 
	<a href="<?= config_item('base_url') ?>help/introduction/publishing-a-page">clicking here</a>.
</p>