<div class="help_title">All About Tooltips (Hint Bubbles)</div>
<p>
	Tooltips are little "hint bubbles" that appear when you place your cursor over certain areas of the page.
	They exist to give you information about the item that your mouse is over.
</p>

<p>
	Tooltips appear for many of the items in the Authoring panel. They are enabled by default.
	Items for which tooltips appear are normally given a light yellow color.
</p>

<p>
	To disable tooltips, click "Turn Off ToolTips" in the upper-right hand part of the authoring panel.
	In the same picture, a tooltip is viewable when the mouse is over the link. Re-enabling tooltips is possible
	by clicking the same link when they are disabled.
<p>

