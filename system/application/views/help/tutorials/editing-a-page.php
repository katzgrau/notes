<div class="help_title">Editing A Page</div>
<p>
	The first step in modifying a page is opening the page for editing. To do that,
	make sure you are in the Edit Pages section of the authoring panel, and click on
	the name of the page you would like to edit in the sidebar on the right. This is pictured below.
</p>
<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-a-page/page-list.png" alt="" title="" />
</p>

<p>
	When you click on a page for editing, the page will be loaded into the content editor. Now you can edit
	the page content, change its title, delete it, save it, publish it, and more. In the image above, the 
	user is about to click on the "homework" page to open it for editing. In the image below, the homework
	page is opened for editing.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-a-page/open-for-editing.png" alt="" title="" />
</p>

<p>
	It is important that you click "Save Draft" after you are completed with your changes. This will will keep
	your changes not visible to the public, but they will be stored for future editing &minus; essentially
	a draft of the future version of the page. You can read more about saving drafts by 
	<a href="<?= config_item('base_url') ?>help/introduction/saving-a-draft">clicking here</a>.
</p>

<p>
	If you want to make changes your page visible to the public, you can click "Publish." You can read more about that
	by <a href="<?= config_item('base_url') ?>help/introduction/publishing-a-page">clicking here</a>.
</p>