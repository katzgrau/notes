<div class="help_title">Inserting A YouTube Video</div>
<p>
	Inserting a video from YouTube (or any online video source) is simple as long as the
	service provides you with the right HTML code. How the code works is not important,
	but it is important that the snippet is provided to you.
</p>

<p class="help_message">
	A video tutorial showing how to insert a YouTube video into a page is available by <a href="<?= config_item('base_url') ?>help/video/insert-youtube-video">clicking here</a>.
</p>

<p>
	On a YouTube video page, there is an information bar on the right side of the page, with a field that says
	"Embed." There is code that can copied and used to embed into your webpage. This code is provided specifically for this purpose.
	It is pictured below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/embed-box.png" />
</p>

<p>
	You will want to highlight and copy that code, like below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/copy.png" />
</p>

<p>
	Now that the code is copied, go back to the authoring panel, and click on the HTML editor button. This button is outlined
	in red below. This button allows you to insert or edit HTML code into the underlying webpage that you are editing.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/html-button.png" />
</p>

<p>
	Once you click the html button, a text box will appear in the center of the screen. If you were editing a blank (or new)
	page, it will be blank. Find an empty line in the box and paste the code like below.
</p>


<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/source-editor.png" />
</p>

<p>
	Click Update. An outline of where the video will be placed on the page will be inserted. The video is not viewable from the editor.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/embedded-video.png" />
</p>	

<p>
	When you publish your page, you will see that your video is embedded into the page when you view it.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-youtube-video/final.png" />
</p>	
