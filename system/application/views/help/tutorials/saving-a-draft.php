<div class="help_title">Saving A Page As A Draft</div>
<p>
	When you are creating a new page or editing a page, you will want to save your progress periodically,
	but you might not want the changes you made to be visible on your website immediately.
</p>

<p>
	For example, let's say Ms. Green is halfway through updating the her homework page
	with this week's assignments, and she wants to save her progress. Because she is only
	halfway done, she does not want students and parents to see changes on her website just yet.
</p>

<p>
	To save her work without making the changes public, all Ms. Green has to do is click
	"Save" in the toolbar above the editor, or the "Save Draft" button on the upper
	right corner of the page. The items you can click to save a draft are outlined in red
	in the image below.
</p>

<p>
	<img src="<?= config_item('base_url') ?>public/help/saving-a-draft/save-options.png" alt="" class="with_picture_frame" />
</p>

<p>
	The authoring panel actually saves your work every 45 seconds in the background, to ensure you do not
	lose any work. But the ability to manually save a draft is important too.
</p>

