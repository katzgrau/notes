<div class="help_title">Inserting A Page Template</div>
<p>
	A page template is a predefined format or layout for something that you want to insert into a page. For example,
	if you wanted to list your contact information, you might want to have it in a table rather than simply list it.
	<?= config_item('company_name') ?> provides a number of templates &minus; some for inserting contact information,
	calendars, weekly schedules, office hours, and more.
</p>

<p>
	Start by opening a new page, or editing an existing one. Make sure your cursor is at the position in the editor
	where you would like the template to appear.
</p>

<p>
	Now click the button labeled "Insert A Template." This button is outlined in red below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-template/template-button.png" />
</p>

<p>
	A menu listing the templates you can insert into the page will appear in the center of the screen. In this example,
	the contact information template will be inserted. Click "Insert" in the row of the template you would like inserted.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-template/template-menu.png" />
</p>

<p>
	The template will appear in the editor, as pictured below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/inserting-a-template/final.png" />
</p>

