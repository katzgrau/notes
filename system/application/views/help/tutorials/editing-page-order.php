<div class="help_title">Rearranging the Page Order on Your Website</div>
<p>
	In the picture below, the user's pages are listed on her website in the order
	of Home, Homework, Extra Help, Contact, and YouTube. Suppose she wants the Contact
	page to be listed last. There is a simple way of doing this.
</p>

<p>
	Go to the Site Settings page, and scroll down to the area labeled "Page Order." This area is outlined in red in the
	picture below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-page-order/page-order.png" />
</p>

<p>
	To rearrange this list, use your mouse to click and drag a page in the list to the position in the list. Please note that
	we are assuming you have more than one page on your website, like the example pictured below.
<p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-page-order/drag.png" />
</p>

<p>
	When you have dragged the page you would like to the correct position in the list, release your mouse button. The pages
	will now have been rearranged on your website. It is not necessary to click "Save Settings."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/editing-page-order/after.png" />
</p>