<div class="help_title">Changing the Title At the Top of Your Website</div>
<p>
		To change the site title, navigate to the site settings page by clicking on the
		Site Settings tab. This is pictured below.
</p>

<p class="help_message">
	The site title is the text that is displayed typically at the top of your website. Changing it does
	not change your website address.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-site-design/site-settings.png" />
</p>

<p>
	Near the top of the page, you will see a field labeled "Site Title" with a corresponding text box. In the example below, 
	the site title is Ms. Green's 5th grade class.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-site-title/site-title.png" />
</p>

<p>
	Change the text to say whatever you would like the title of your website to be. Then press the Enter key, or click
	"Save Settings," as pictured below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/changing-site-title/save.png" />
</p>

<p>
	Once it is saved, your site title will have been changed. Below are before and after pictures.
</p>

