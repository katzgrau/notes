<div class="help_title">Unpublishing A Page</div>
<p>
	Unpublishing a page is handy for when you do want make a page on your website invisible,
		but you either want to save the content for future publication, or simply do not want to lose it.
</p>

<p class="help_message">
	Unpublishing a page will only make it invisible. You will not lose any work, attchments, or pictures associated with the page.
</p>

<p>
	A teacher, for instance, might teach Spanish 1 for the first half of the school year, and
	Spanish 2 for the second half of the year. When the second half of the year comes along,
	the teacher may not want to completely delete the Spanish 1 page $mius; she just wants to 
	make it invisible to the publish.
</p>

<p>
	This is a perfect time for unpublishing a page. To unpublish a page, open a page for editing, and click the 
	"Unpublish" button, which is outlined in red below.
</p>

