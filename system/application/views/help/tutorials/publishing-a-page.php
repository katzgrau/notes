<div class="help_title">Publishing A Page</div>
<p>
	After you have edited a page, and are ready to make your changes viewable to all
	who visit your website, you will want to publish that page.
</p>

<p>
	To publish a page that you have open for editing, simple click on
	the "Publish" button in the upper right corner of the page, or click
	on the "Publish" link in the toolbar above the content area.
</p>

<p>
	The image below shows these two buttons outlined in red.
</p>

<p>
	<img src="<?= config_item('base_url') ?>public/help/publishing-a-page/publish-buttons.png" alt="" class="with_picture_frame" />
</p>

<p>
	Once you publish a page, you can click "View Website" to see that
	your changed are indeed visible on your website.
</p>

<p>
	You can make published pages invisible by opening that page for editing,
	then clicking on "Unpublish." You can read more about that by
	<a href="<?= config_item('base_url') ?>help/introduction/unpublishing-a-page">clicking here</a>.
</p>