<div class="help_title">Deleting A Page</div>
<p>
	When you no longer want or need a web page, you are able to delete it from your website. 
</p>

<p class="help_warning">
	Deleting the page
	will permenantly remove that page, all attachments, and pictures uploaded to that page. It cannot be
	undone.
</p>

<p>
	The "Delete Page" button is outlined below in red.
</p>

<p>
<img src="<?= config_item('base_url') ?>public/help/deleting-a-page/delete-page.png" alt="" class="with_picture_frame" />
</p>

<p>
	If you wish to keep the contents of a page on your website, but you do not want the page to be visible, you do not have to 
	delete it. You might only want to unpublish it. You can read more about that by <a href="<?= config_item('base_url') ?>help/introduction/unpublishing-a-page">clicking here</a>.
</p>