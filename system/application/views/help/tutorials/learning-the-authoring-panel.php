<a name="editpages"></a>
<p><strong>Edit Pages</strong></p>
<p>
	The page editing section of the authoring panel is where you manage everything in terms of page content, adding pages,
	removing pages, and more. If it is your first time logging in, your website will have one page in it, which was created
	automatically when you signed up.
</p>
<p>Below is the authoring panel of the fictional user ms-green.</p>
<p>
<img class="with_picture_frame" width="800" src="<?= config_item('base_url') ?>public/help/common/author-editor-screenshot.png" alt="" title="" />
</p>
<p>Below is an image of the same authoring panel, with labels (which are described following the image)</p>
<p>
<img class="with_picture_frame" width="800" src="<?= config_item('base_url') ?>public/help/common/author-editor-screenshot-labeled.png" alt="" title="" />
</p>
<p>
	Here is a description of each of the labels above.
</p>
<ul>
	<li>A - This is where you can edit the title of the page you are editing or creating</li>
	<li>
		B - This is a shortcut bar that will let you add a new page to your site, insert an image into the page,
		attach a file to the page, insert a template into the page, save the page as a draft, or publish it to 
		your website
	</li>
	<li>C - The is the text editor, where you edit the content of the page</li>
	<li>D - This is the area where you can attach files to the page</li>
	<li>E - This is the area where you can unpublish a published page, or revert an updated draft to the published version</li>
	<li>F - This is a listing of the pages on your website. Click on of these pages will open it for editing in the text area</li>
	<li>G - This is where you can save the current page you are editing as a draft, or publish it</li>
</ul>
