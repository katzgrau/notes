<a name="introduction"></a>
<p><strong>Introduction</strong></p>
<p>
	Now that you have a <?= config_item('company_name') ?> account, we want to show you around what we call
	the "authoring panel." The authoring panel is where you edit everything about your website, including the 
	content, pictures, links, design, and more.
</p>

<p>
	There are 4 major areas in the authoring panel: <a href="#desktop">Desktop</a>, <a href="#editpages">Page Editor</a>, 
	<a href="#settings">Site Settings</a>, and <a href="#account">Account Editor</a>, and Help Section. 
	Here is a brief description of what each of these areas does.
</p>

<ul class="diagram_listing">
	<li><a href="#desktop">Desktop</a>: Provides a menu to the Page Editor, Site Settings Area, Account Area, and Help Section</li>
	<li><a href="#editpages">Edit Pages</a>: This is where you can add pages to your site, edit them, delete them, upload images, and more</li>
	<li><a href="#settings">Site Settings</a>: This is where you can change your site's design, add links, and rearrange page listing order</li>
	<li><a href="#account">Edit Account</a>: Where you change your password, email address, and other account information</li>
	<li>Help: Where you find tutorials on how to use the functions above</li>
</ul>

<a name="tooltips"></a>
<p><strong>Tooltips (Hint Bubbles)</strong></p>
<p>
By default, hovering your mouse over different areas of the authoring panel will cause help bubbles, or "tooltips"
to explain to you what that area of the authoring panel does. Typically, the areas which you can hover your mouse
to view a tip over are colored light yellow.
</p>
<p>
At any time, you can disabled this feature by clicking "Turn Off Tooltips" in the upper-right-hand corner of the authoring panel.
The Image below shows the user, who has tooltips enabled, about to disable the feature.
</p>
<p>
<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/common/author-tooltips-screenshot.png" alt="" title="" />
</p>
<p>
While you are getting used to the authoring panel, it is recommended that you keep tooltips enabled to receive many useful
bits of information.
</p>


<a name="desktop"></a>
<p><strong>The Desktop</strong></p>
<p>
	When you log in, your are taken to the Desktop area of the authoring panel. This is essentially a menu
	of the different areas of the authoring panel. Clicking on one of these choices with take you
	to that respective area.
</p>
<p>
	Keep in mind that although you can use the desktop to access the areas of the authoring panel,
	you can also use the navigation menu at the top of the page to jump around as well. This navigation
	bar will be displayed wherever you are in the authoring panel.
</p>
