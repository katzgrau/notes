<a name="settings"></a>
<p><strong>Site Settings</strong></p>
<p>
	The site settings section allows you to edit things not specifically associated with a given page of
	your website. For example, this is where you can change your website's design, among other things.
</p>
<p>Below is the site settings page.</p>
<p>
<img class="with_picture_frame" width="800" src="<?= config_item('base_url') ?>public/help/common/author-settings-screenshot.png" alt="" title="" />
</p>
<p>
	Below is the same page labeled with descriptions of each label after.
</p>
<p>
<img class="with_picture_frame" width="800" src="<?= config_item('base_url') ?>public/help/common/author-settings-screenshot-labeled.png" alt="" title="" />
</p>
<ul>
	<li>A - This is where you can edit to the title of your website (which is usually displayed at the top of your site in large text)</li>
	<li>B - This is where you can change the design of your site. Click on a design to preview your website with that style, and click "Activate" to use it.
		You can also click "Get Next" to see another page of designs.
	</li>
	<li>C - This is where you can rearrange the order of the pages on your site. Click and drag a page name to position of
		the list you would like it to be in.
	</li>
	<li>
		D - This is where you can rearrange the order of the links on your site. Click and drag a link name to position of
		the list you would like it to be in.
	</li>
	<li>
		E - Add a link to your website here. The the URL (or web address) in the top box, and the link title in the second.
		Finally, click "Add Link."
	</li>
</ul>
