<div class="help_title">Attaching A File to a Page</div>
<p class="help_warning">
	Before you begin, you must make sure that you
	open an existing page for editing. You may open a new page, but you must save it as a draft first.
</p>

<p>
	To attach a file to a page, open a page for editing. Scroll to the bottom of the page, and you will see an
	area labeled "attachments." Click on the "Browse" button, which is outlined in red below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/attaching-a-file/browse.png" />
</p>

<p>
	A window to browse for the file you would like to attach to the page will appear. Browse for the file you would
	like to attach to the page, as pictured below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/attaching-a-file/save-as.png" />
</p>

<p>
	Next, click "Attach File," which is outlined in red below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/attaching-a-file/attach.png" />
</p>
<p class="help_warning">
	There is an upload limit of 2 megabytes. This is sufficient for most files, with the exception of large media files,
	like video, and raw digital camera pictures. If an error appears, it is likely that your file was too big.
</p>
<p>
	The page will refresh, which may take a few moments. The file is being copied from your computer to your webpage. Once
	the process is complete, your file will be listed underneath the browse button. It will also be automatically linked
	to your page. Notice how the file attached in the example is now visible on the user's page below.
</p>



