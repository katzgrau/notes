<div class="help_title">Creating A Table</div>
<p>
	A table is great for charts and tabular information. To crate a table, the first thing you will want to do is have a
	page open for editing, and make sure the cursor is positioned where you want the table to be inserted.
</p>

<p>
	The button to click is outlined in red below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-table/table-button.png" />
</p>

<p>
	When you click on that button, a window toolbox will appear in the center of the screen. This is pictured below.
	You can choose the number of columns and rows that you want in your table by editing "Cols," and "Rows." It is also recommended that
	you edit the width field to read "100%." When you are done, click the "Insert."
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-table/table-window.png" />
</p>

<p>
	After you click "Insert," you will see a table similar to the one below.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-table/new-table.png" />
</p>

<p>
	You can you your mouse to click and drag the table,
	resizing it. You can also enter content into each cell, align the paragraphs, and more.
</p>

<p>
	<img class="with_picture_frame" src="<?= config_item('base_url') ?>public/help/creating-a-table/new-table-edited.png" />
</p>